export const getStarsByConstellation = (id: string) =>
  stars.filter(s => s.constellation === id);

export const getConstellationById = (id: string) =>
  constellations.find(c => c.id === id);

export const getStarById = (id: string) =>
  stars.find(s => s.id === id);

export const getStarsWithExoplanets = () =>
  stars.filter(s => s.exoplanets && s.exoplanets.length > 0);

export const getHabitableExoplanets = () =>
  stars.filter(s => s.exoplanets?.some(p => p.habitable));

export const getStarColor = (spectralClass: SpectralClass): string =>
  SPECTRAL_COLORS[spectralClass] ?? '#ffffff';

export const raDecToXY = (ra: number, dec: number): { x: number; y: number } => {
  const raRad = (ra / 24) * 2 * Math.PI;
  const decRad = (dec * Math.PI) / 180;
  const r = Math.cos(decRad);
  const x = r * Math.sin(raRad);
  const y = -r * Math.cos(raRad);
  const scale = 1 / Math.cos((30 * Math.PI) / 180);
  return {
    x: x * scale * 0.48,
    y: y * scale * 0.48 - dec * 0.003,
  };
};
